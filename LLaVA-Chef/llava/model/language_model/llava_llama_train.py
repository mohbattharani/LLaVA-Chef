class Metrics_Huggingface ():
    def __init__ (self):
        self.rouge = evaluate.load('rouge')
        self.bleu = evaluate.load("bleu")
        self.sacrebleu = evaluate.load("sacrebleu")
        
    def compute (self, labels, preds):
        scores = {}
        scores['rougeL'] = self.rouge.compute(predictions=preds, references=labels)['rougeL']
        scores['bleu'] = self.bleu.compute(predictions=preds, references=labels)['bleu']
        scores['sacrebleu'] = self.sacrebleu.compute(predictions=preds, references=labels)['score']
        
        return scores
    

